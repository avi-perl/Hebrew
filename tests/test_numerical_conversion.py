def _convert(
    number: int,
    geresh: bool = True,
    punctuate: bool = True,
    substitutions: Optional[SubstitutionFlag] = Substitutions.DEFAULT,
):
    """
    Convert the nuumber while testing that the conversion is the same as the
    conversion from the Hebrew class.
    """
    s1 = number_to_hebrew_string(
        number,
        geresh=geresh,
        punctuate=punctuate,
        substitutions=substitutions,
    )
    s2 = str(
        Hebrew.from_number(
            number,
            geresh=geresh,
            punctuate=punctuate,
            substitutions=substitutions,
        )
    )
    assert s1 == s2
    return s1


def test_numerical_conversion():
    """
    Test the conversion of numbers to Hebrew strings.
    """
    (
        with_geresh,
        without_apostrophe,
        without_punctuation,
        davka_without_punctuation,
        davka_without_punctuation_polite,
    ) = (
        test_data["withGeresh"],
        test_data["withoutApostrophe"],
        test_data["withoutPunctuation"],
        test_data["davkaWithoutPunctuation"],
        test_data["davkaWithoutPunctuationPolite"],
    )

    # Test Entire Numbers
    for number, expected in with_geresh.items():
        assert _convert(int(number)) == expected
    for number, expected in without_apostrophe.items():
        assert _convert(int(number), geresh=False) == expected
    for number, expected in davka_without_punctuation_polite.items():
        assert (
            _convert(int(number), punctuate=False, substitutions=Substitutions.ALL)
            == expected
        )
    for number, expected in davka_without_punctuation.items():
        assert _convert(int(number), punctuate=False) == expected
    for number, expected in without_punctuation.items():
        assert _convert(int(number), punctuate=False) == expected

    # Generate 10,000 numbers between 0 and 1,000,000 test using the last 3 digits
    # of each number.
    random_numbers = [random.randint(1, 1000000) for _ in range(10000)]
    for number in random_numbers:
        last_three_digits = str(number)[-3:].lstrip("0")
        if last_three_digits == "":
            continue
        assert _convert(int(last_three_digits)).endswith(with_geresh[last_three_digits])
        assert _convert(int(last_three_digits), geresh=False).endswith(
            without_apostrophe[last_three_digits]
        )
        assert _convert(int(last_three_digits), punctuate=False).endswith(
            davka_without_punctuation[last_three_digits]
        )
        assert _convert(
            int(last_three_digits), punctuate=False, substitutions=Substitutions.ALL
        ).endswith(davka_without_punctuation_polite[last_three_digits])

        assert _convert(int(last_three_digits), punctuate=False).endswith(
            without_punctuation[last_three_digits]
        )
